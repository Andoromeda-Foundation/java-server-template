CREATE TABLE `event_logs` (
  `id` BIGINT(20) NOT NULL AUTO_INCREMENT,
  `occurred_at` DATETIME NULL,
  `content` VARCHAR(255) NULL,
  `power_station_id` BIGINT(20) NULL,
  `confirmed` TINYINT(1) NULL DEFAULT '0',
  `create_by` BIGINT(20) NULL DEFAULT NULL,
  `create_at` DATETIME NULL DEFAULT CURRENT_TIMESTAMP COMMENT 'CURRENT_TIMESTAMP',
  `update_by` BIGINT(20) NULL DEFAULT NULL,
  `update_at` DATETIME NULL DEFAULT CURRENT_TIMESTAMP,
  `delete_by` BIGINT(20) NULL DEFAULT NULL,
  `delete_at` DATETIME NULL DEFAULT CURRENT_TIMESTAMP,
  `active` TINYINT(1) NULL DEFAULT '1',
  `deleted` TINYINT(1) NULL DEFAULT '0',
  PRIMARY KEY (`id`));
