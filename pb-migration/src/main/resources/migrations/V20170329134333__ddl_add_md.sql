CREATE TABLE `mds` (
  `id` BIGINT(20) NOT NULL AUTO_INCREMENT,
  `p` DECIMAL(20,4) NULL DEFAULT NULL,
  `q` DECIMAL(20,4) NULL DEFAULT NULL,
  `value` DECIMAL(20,4) NULL DEFAULT NULL,
  `name` VARCHAR(45) NULL DEFAULT NULL,
  `node` VARCHAR(45) NULL DEFAULT NULL,
  `uuid` VARCHAR(128) NULL DEFAULT NULL,
  `redis_key` VARCHAR(256) NOT NULL,
  `metric_at` DATETIME NULL DEFAULT NULL,
  `create_by` BIGINT(20) NULL DEFAULT NULL,
  `create_at` DATETIME NULL DEFAULT CURRENT_TIMESTAMP,
  `update_by` BIGINT(20) NULL DEFAULT NULL,
  `update_at` DATETIME NULL DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
  `delete_by` BIGINT(20) NULL DEFAULT NULL,
  `delete_at` DATETIME NULL DEFAULT NULL,
  `active` TINYINT(1) NULL DEFAULT '1',
  `deleted` TINYINT(1) NULL DEFAULT '0',
  PRIMARY KEY (`id`));
