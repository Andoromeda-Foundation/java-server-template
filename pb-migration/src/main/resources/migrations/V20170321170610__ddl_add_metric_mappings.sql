CREATE TABLE plc_metric_filter_mappings (
  `id` BIGINT(20) NOT NULL AUTO_INCREMENT,
  `uuid` VARCHAR(128) NOT NULL,
  `name` VARCHAR(45) NULL,
  `node` VARCHAR(45) NULL,
  `metric_at` DATETIME NULL,
  `queue_name` VARCHAR(200) NULL,
  `quere_param` VARCHAR(200) NULL,
  `create_by` BIGINT(20) NULL DEFAULT NULL,
  `create_at` TIMESTAMP NULL DEFAULT CURRENT_TIMESTAMP,
  `update_by` BIGINT(20) NULL DEFAULT NULL,
  `update_at` TIMESTAMP NULL DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
  `delete_by` BIGINT(20) NULL DEFAULT NULL,
  `delete_at` TIMESTAMP NULL DEFAULT CURRENT_TIMESTAMP,
  `active` TINYINT(1) NULL DEFAULT '1',
  `deleted` TINYINT(1) NULL DEFAULT '0',
  PRIMARY KEY (`id`));
